    public void init() {
        setTitle("Quản lý thể loại gạch");
        setLocationRelativeTo(null);
        dtm = (DefaultTableModel) tblTheLoai.getModel();
        dtm.setRowCount(0);
        fillDS();
    }

    public void fillDS() {
        dtm.setRowCount(0);
        ArrayList<TheLoai> list = daoTL.selectAll();
        try {
            for (TheLoai tl : list) {
                dtm.addRow(new Object[]{tl.getMaTheLoai(), tl.getTenTheLoai()});
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "lỗi truy vẫn dữ liệu");
        }
        
    }
    void setForm(TheLoai tl){
        txtMaTheLoai.setText(tl.getMaTheLoai());
        txtTenTheLoai.setText(tl.getTenTheLoai());
    }
